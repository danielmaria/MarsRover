package plateau;

public class Plateu {
	private int dimensionX;
	private int dimensionY;
	
	public Plateu(int x, int y){
		this.dimensionX = x;
		this.dimensionY = y;
	}

	public int getDimensionX() {
		return dimensionX;
	}

	public int getDimensionY() {
		return dimensionY;
	}
	
}
